#include<iostream>
using namespace std;
int main()
{
    int n;
    cin>>n;
    while(1)
    {
        switch(n)
        {
            case 1:cout<<"One"<<endl;
            exit(0);
            case 2: cout<<"Two"<<endl;
            exit(0);
            case 3: cout<<"three"<<endl;
            exit(0);
            default : cout<<"No case "<<endl;
            exit(0);
        }
        
    }
    return 0;
}